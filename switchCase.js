"use strict";

const ps = require("prompt-sync");
const prompt = ps();

let number = prompt("Enter a single digit number : ");

switch (number) {

    case 1:
        console.log("One");
        break;
    case 2:
        console.log("Two");
        break;
    case 3:
        console.log("Three");
        break;
    case 4:
        console.log("Four");
        break;
    case 5:
        console.log("Five");
        break;
    case 6:
        console.log("Six");
        break;
    case 7:
        console.log("Seven");
        break;
    default:
        console.log('invalid');
}

